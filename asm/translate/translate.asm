section .text
global _start
_start:
	; create stack frame
	push ebp
	mov ebp, esp
	push ebx
	push esi

.read:
	; read from stdin to buff
	; sys_read syscall
	mov eax, 0x03 ; syscall id
	mov ebx, 0x00 ; stdin
	mov ecx, buff ; where to store the data
	mov edx, buff_len
	int 0x80
	mov esi, eax
	test esi, esi
	jz .quit

	;; TODO: Process the data in buff.
	xor ecx, ecx
	jmp .l1
.l2:
	mov ebx, buff
	mov al, [ebx+ecx]
	movzx eax, al
	mov ebx, xlat_table
	mov dl, [ebx+eax]
	mov ebx, buff
	mov [ebx+ecx], dl
	inc ecx
.l1:
	cmp ecx, esi
	jl .l2

	; write from buff to stdout
	; sys_write syscall
	mov eax, 0x04 ; syscall id
	mov ebx, 0x01 ; stdout
	mov ecx, buff  ; from where to read
	mov edx, esi
	int 0x80
	jmp .read
	
	; destroy stack frame
	lea esp, [ebp-0x08]
	pop esi
	pop ebx
	pop ebp

.quit:
	; exit the application
	; sys_exit syscall
	mov eax, 0x01 ; syscall id
	mov ebx, 0x00 ; exit code
	int 0x80


